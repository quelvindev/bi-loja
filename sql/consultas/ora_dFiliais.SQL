SELECT
    PCFILIAL.CODIGO COD_FILIAL,
    PCFILIAL.RAZAOSOCIAL RAZAO_SOCIAL,
    PCFILIAL.FANTASIA,
    PCFILIAL.CODIGO||'-'||PCFILIAL.RAZAOSOCIAL COD_RAZAO,
    PCFILIAL.CODIGO||'-'||PCFILIAL.FANTASIA COD_FANTASIA,
    DECODE(PCFILIAL.CODIGO,1,'FILIAL - 1',2,'FILIAL - 2',3,'FILIAL - 3',4,'FILIAL - 4',5,'FILIAL - 5',6,'CD',8,'FILIAL - 8','XXX') COD_FANTASIA_RED,    
    PCFILIAL.CGC CNPJ,
    PCFILIAL.CODCLI COD_CLI_FILIAL,
    PCFILIAL.CODFORNEC COD_FORNEC_FILIAL
FROM 
    PCFILIAL
WHERE 
    PCFILIAL.CODIGO NOT IN(7,99)
ORDER BY 
    COD_FILIAL